    //log in 
    private void loginSaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginSaveBtnActionPerformed
        try{
            String userName = loginNameTextField.getText(); //get the user input
            String password = String.valueOf(loginPasswordTxtField.getPassword());
                
            if(!userName.equals("") && !password.equals("")){   //if not blank
                if(loginAdminRadioBtn.isSelected()){    //log in base on the position selected
                    if(new LogInProcess().logInPanel(userName, password, false)){   //if the username and password matches to an existing account
                        dispose();  //dispose the fram and go to main menu admin frame
                        MainMenuAdmin mainMenuAdmin = new MainMenuAdmin();
                        mainMenuAdmin.setVisible(true);
                        
                    }
                    else
                        JOptionPane.showMessageDialog(rootPane, "Wrong Username or Password!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else if(loginCashierRadioBtn.isSelected()){ //log in base on the position selected
                    if(new LogInProcess().logInPanel(userName, password, true)){    //if the username and password matches to an existing account
                        dispose();
                        MainMenuCashier cashier = new MainMenuCashier();
                        cashier.setVisible(true);
                    }
                    else
                        JOptionPane.showMessageDialog(rootPane, "Wrong Username or Password!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else
                    JOptionPane.showMessageDialog(rootPane, "Please Select Cashier of Admin", "Warning", JOptionPane.WARNING_MESSAGE);
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Please fill up the following", "Warning", JOptionPane.WARNING_MESSAGE);
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, "An Error has occured!", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_loginSaveBtnActionPerformed


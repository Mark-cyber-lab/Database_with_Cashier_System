    // this is for adding the product in the order list
    private void cashierAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashierAddBtnActionPerformed
        try{
            UPC = Long.parseLong(cashierAddUPCTxtField.getText());  //gets the text in the textfield for the adding of the product in the order list
            amount = Long.parseLong(cashierAddAmountTxtField1.getText());
            
            if(amount > 0){ //chacks if the amount entered is valid
                if(!checkExisting(UPC)){    //checks if there is a same existing product in the list
                    backBone.add(UPC, amount);  //perform add in the orderlist
                    setTables();    //set the table for the new product
                }
                cashierAdd.setVisible(false);   //goes back to the main panel of the cashier system
                cashierMain.setVisible(true);
                cashierAddUPCTxtField.setText("");  //sets the text to blank
                cashierAddAmountTxtField1.setText("");
            }
            else
                JOptionPane.showMessageDialog(null, "Invalid amount input!", "Warning", JOptionPane.WARNING_MESSAGE);
        }catch (NumberFormatException e){   //this will catch the invalid input
            JOptionPane.showMessageDialog(null, "Invalid Input Format!", "Alert", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_cashierAddBtnActionPerformed

    //checks if there is a same existing product in the order list
    private boolean checkExisting(long UPC){    //returns true if there is an identical product
        for(Product product: backBone.order){   //iterate in order to see if there is a same existing product in the list
            if(product.getUniversalProductKey() == UPC){    //prompts the user if there is an identical product
                JOptionPane.showMessageDialog(rootPane, "Product already exist in the list!", "Alert", JOptionPane.WARNING_MESSAGE);
                return true;
            }
        }
        return false;
    }
    
    //set the ordered products in the table
    private void setTables(){
        DefaultTableModel model = (DefaultTableModel) cashierTable1.getModel(); //get the model of the table and set its row to zero
        model.setRowCount(0);
        double total = 0;   //resets the total to 0
        for(Product c: backBone.order){ //iterate and add the product in the table
            String[] toBeAdded = {c.getBrandName(), c.getType(), c.getName(), "" + c.getAmount(), "" + c.getPrice() , "" + c.getPrice() * c.getAmount()};
            model.addRow(toBeAdded);
            total += c.getPrice() * c.getAmount();
        }     
        totalValue.setText("" + total); //sets the text field to blank
    }
    
    //checks out the ordered products in the ordered list
    private void cashierCheckOutConfirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashierCheckOutConfirmBtnActionPerformed
        total = Double.parseDouble(totalValue.getText());   //get the total cost of the product
        try{
            payment = Double.parseDouble(cashierCheckoutCashTxtField.getText());    //gets the payment received from the user
            if(payment < total) //checks if the payment is sufficient
                JOptionPane.showMessageDialog(rootPane, "Payment insufficient!", "Error", JOptionPane.ERROR_MESSAGE);
            else{
                Receipt receipt = new Receipt(backBone.order, payment); //instantiate a receipt object that will print the receipt of the current transactions
                receipt.setVisible(true);
                changeField.setText((payment - Double.parseDouble(totalValue.getText())) + "");
                cashierCheckOutConfirmBtn.setVisible(false); //for the added effects
                cashierCheckOutCancelBtn.setVisible(false);
                cashierCheckoutCashTxtField.setEditable(false);
                cashierCheckoutCashTxtField.setFocusable(false);
                receipt.setDefaultCloseOperation(DISPOSE_ON_CLOSE); //so that the program will not terminate even if the frame of the receip is clicked on X
                backBone.recordTransaction(total, payment); //records the transaction
                backBone.saveChanges(); //save the changes in the product's stocks
                setTables();
            }
        }catch(IOException e){  //if there is a problem encountered
            JOptionPane.showMessageDialog(rootPane, "An error has occured!", "Alert", JOptionPane.ERROR_MESSAGE);
            cashierCheckOut.setVisible(false);
            cashierMain.setVisible(true);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Invalid Input Format!", "Alert", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cashierCheckOutConfirmBtnActionPerformed

    

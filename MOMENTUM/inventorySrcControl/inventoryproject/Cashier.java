    //this is for reversing the array of the selected items in the ordered list
    private void reverseArray(int[] array){
        for(int i = 0; i < array.length / 2; i++){  //reverses the array
            int temp = array[i];
            array[i] = array[array.length - i - 1];
            array[array.length - i - 1] = temp;
        }
    }
    
    //deletes the selected items in the ordered list
    private void cashierDeleteBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashierDeleteBtn1ActionPerformed
        int[] selectedIndex = cashierTable1.getSelectedRows();  //gets the selected 
        if(selectedIndex.length > 0){   //checks if the user selected a product in the ordered list
            Arrays.sort(selectedIndex, 0, selectedIndex.length);    //sorts the list of the selected index in the ordered products
            reverseArray(selectedIndex);    //reverse the array for the proper deletion
            if(JOptionPane.showConfirmDialog(null, "Contnue?", "Confirmation", JOptionPane.OK_CANCEL_OPTION) == 0){
                for(int c: selectedIndex)   //perform deletion
                    backBone.delete(c);
                setTables();    //sets the values of the table
            }
            else
                JOptionPane.showMessageDialog(null, "Operation has been cancelled!");
        }
        else
            JOptionPane.showMessageDialog(null, "Please select a product!", "Warning", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_cashierDeleteBtn1ActionPerformed

